def _sample_2d_strip(
    fn,
    n: int = 200,
    x_start: float = 0.0,
    x_step: float = 0.01,
    y_fixed: float = 0.5,
    **kwargs,
) -> list[float]:
    """Sample a 2D function along a 1D strip at constant y."""
    return [fn((x_start + i * x_step, y_fixed), **kwargs) for i in range(n)]


def _sample_3d_strip(
    fn,
    n: int = 200,
    x_start: float = 0.0,
    x_step: float = 0.01,
    y_fixed: float = 0.5,
    z_fixed: float = 0.5,
    **kwargs,
) -> list[float]:
    """Sample a 3D function along a 1D strip at constant y, z."""
    return [
        fn((x_start + i * x_step, y_fixed, z_fixed), **kwargs)
        for i in range(n)
    ]


def _adjacent_variation(samples: list[float]) -> float:
    """Mean absolute difference between adjacent samples."""
    if len(samples) < 2:
        return 0.0
    return sum(abs(samples[i] - samples[i + 1]) for i in range(len(samples) - 1)) / (len(samples) - 1)


def _mean(samples: list[float]) -> float:
    """Sample mean."""
    return sum(samples) / len(samples) if samples else 0.0


def _variance(samples: list[float]) -> float:
    """Sample variance (unbiased)."""
    if len(samples) < 2:
        return 0.0
    m = _mean(samples)
    return sum((v - m) ** 2 for v in samples) / (len(samples) - 1)


def _zero_crossings(samples: list[float]) -> int:
    """Count sign changes between adjacent samples."""
    return sum(1 for i in range(len(samples) - 1) if samples[i] * samples[i + 1] < 0)


