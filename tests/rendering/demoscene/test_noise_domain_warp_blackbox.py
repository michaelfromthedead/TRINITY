# --- WGSL primitive helpers ---

def wgsl_fract(x: float) -> float:
    """WGSL fract: x - floor(x)."""
    return x - math.floor(x)


def wgsl_lerp(a: float, b: float, t: float) -> float:
    """Linear interpolation."""
    return a + t * (b - a)


def wgsl_smoothstep(t: float) -> float:
    """Smoothstep fade: 6t^5 - 15t^4 + 10t^3."""
    return t * t * t * (t * (t * 6.0 - 15.0) + 10.0)


# --- Hash functions (from T-DEMO-1.28, spec: Positional Hash) ---

def py_hash11(p: float) -> float:
    """1D hash: float -> [0, 1)."""
    q = p
    q = wgsl_fract(q * 0.1031)
    q = q * (q + 33.33)
    q = q * (q + q)
    return wgsl_fract(q)


def py_hash21(p) -> float:
    """2D hash: (x, y) -> [0, 1)."""
    qx = wgsl_fract(p[0] * 0.1031)
    qy = wgsl_fract(p[1] * 0.1030)
    d = qx * (qx + 33.33) + qy * (qy + 33.33)
    qx = qx + d
    qy = qy + d
    return wgsl_fract(qx * qy)


def py_hash31(p) -> float:
    """3D hash: (x, y, z) -> [0, 1)."""
    qx = wgsl_fract(p[0] * 0.1031)
    qy = wgsl_fract(p[1] * 0.1030)
    qz = wgsl_fract(p[2] * 0.0973)
    d = qx * (qx + 33.33) + qy * (qy + 33.33) + qz * (qz + 33.33)
    qx = qx + d
    qy = qy + d
    qz = qz + d
    return wgsl_fract(qx * qy * qz)


# --- Value noise base functions (from T-DEMO-1.29, spec: Value Noise Interpolation) ---

def py_value_noise_1d(p: float) -> float:
    """Value noise 1D: hash interpolation."""
    i = math.floor(p)
    f = p - i
    u = wgsl_smoothstep(f)
    a = py_hash11(i)
    b = py_hash11(i + 1.0)
    return wgsl_lerp(a * 2.0 - 1.0, b * 2.0 - 1.0, u)


def py_value_noise_2d(p) -> float:
    """Value noise 2D: bilinear hash interpolation."""
    ix = math.floor(p[0])
    iy = math.floor(p[1])
    fx = p[0] - ix
    fy = p[1] - iy
    ux = wgsl_smoothstep(fx)
    uy = wgsl_smoothstep(fy)
    a = py_hash21((ix, iy)) * 2.0 - 1.0
    b = py_hash21((ix + 1.0, iy)) * 2.0 - 1.0
    c = py_hash21((ix, iy + 1.0)) * 2.0 - 1.0
    d = py_hash21((ix + 1.0, iy + 1.0)) * 2.0 - 1.0
    return wgsl_lerp(wgsl_lerp(a, b, ux), wgsl_lerp(c, d, ux), uy)


def py_value_noise_3d(p) -> float:
    """Value noise 3D: trilinear hash interpolation."""
    ix = math.floor(p[0])
    iy = math.floor(p[1])
    iz = math.floor(p[2])
    fx = p[0] - ix
    fy = p[1] - iy
    fz = p[2] - iz
    ux = wgsl_smoothstep(fx)
    uy = wgsl_smoothstep(fy)
    uz = wgsl_smoothstep(fz)
    a = py_hash31((ix, iy, iz)) * 2.0 - 1.0
    b = py_hash31((ix + 1.0, iy, iz)) * 2.0 - 1.0
    c = py_hash31((ix, iy + 1.0, iz)) * 2.0 - 1.0
    d = py_hash31((ix + 1.0, iy + 1.0, iz)) * 2.0 - 1.0
    e = py_hash31((ix, iy, iz + 1.0)) * 2.0 - 1.0
    f = py_hash31((ix + 1.0, iy, iz + 1.0)) * 2.0 - 1.0
    g = py_hash31((ix, iy + 1.0, iz + 1.0)) * 2.0 - 1.0
    h = py_hash31((ix + 1.0, iy + 1.0, iz + 1.0)) * 2.0 - 1.0
    return wgsl_lerp(
        wgsl_lerp(wgsl_lerp(a, b, ux), wgsl_lerp(c, d, ux), uy),
        wgsl_lerp(wgsl_lerp(e, f, ux), wgsl_lerp(g, h, ux), uy),
        uz,
    )


# --- Perlin noise 3D (from T-DEMO-1.30, spec: Perlin Noise Gradient) ---

_GRADIENTS: list[tuple[float, float, float]] = [
    (1.0, 1.0, 0.0), (-1.0, 1.0, 0.0), (1.0, -1.0, 0.0), (-1.0, -1.0, 0.0),
    (1.0, 0.0, 1.0), (-1.0, 0.0, 1.0), (1.0, 0.0, -1.0), (-1.0, 0.0, -1.0),
    (0.0, 1.0, 1.0), (0.0, -1.0, 1.0), (0.0, 1.0, -1.0), (0.0, -1.0, -1.0),
]

INV_SQRT2 = 0.7071067811865475


def _py_perlin_gradient(hash_value: float, offset) -> float:
    """Select gradient vector from hash and dot with offset."""
    h = int(hash_value * 12.0) % 12
    gx, gy, gz = _GRADIENTS[h]
    return INV_SQRT2 * (gx * offset[0] + gy * offset[1] + gz * offset[2])


def py_perlin_noise_3d(p) -> float:
    """Perlin noise 3D: gradient-based."""
    ix = math.floor(p[0])
    iy = math.floor(p[1])
    iz = math.floor(p[2])
    fx = p[0] - ix
    fy = p[1] - iy
    fz = p[2] - iz
    ux = wgsl_smoothstep(fx)
    uy = wgsl_smoothstep(fy)
    uz = wgsl_smoothstep(fz)
    o000 = (fx, fy, fz)
    o100 = (fx - 1.0, fy, fz)
    o010 = (fx, fy - 1.0, fz)
    o110 = (fx - 1.0, fy - 1.0, fz)
    o001 = (fx, fy, fz - 1.0)
    o101 = (fx - 1.0, fy, fz - 1.0)
    o011 = (fx, fy - 1.0, fz - 1.0)
    o111 = (fx - 1.0, fy - 1.0, fz - 1.0)
    h000 = py_hash31((ix, iy, iz))
    h100 = py_hash31((ix + 1.0, iy, iz))
    h010 = py_hash31((ix, iy + 1.0, iz))
    h110 = py_hash31((ix + 1.0, iy + 1.0, iz))
    h001 = py_hash31((ix, iy, iz + 1.0))
    h101 = py_hash31((ix + 1.0, iy, iz + 1.0))
    h011 = py_hash31((ix, iy + 1.0, iz + 1.0))
    h111 = py_hash31((ix + 1.0, iy + 1.0, iz + 1.0))
    g000 = _py_perlin_gradient(h000, o000)
    g100 = _py_perlin_gradient(h100, o100)
    g010 = _py_perlin_gradient(h010, o010)
    g110 = _py_perlin_gradient(h110, o110)
    g001 = _py_perlin_gradient(h001, o001)
    g101 = _py_perlin_gradient(h101, o101)
    g011 = _py_perlin_gradient(h011, o011)
    g111 = _py_perlin_gradient(h111, o111)
    vx00 = g000 + ux * (g100 - g000)
    vx10 = g010 + ux * (g110 - g010)
    vx01 = g001 + ux * (g101 - g001)
    vx11 = g011 + ux * (g111 - g011)
    vy0 = vx00 + uy * (vx10 - vx00)
    vy1 = vx01 + uy * (vx11 - vx01)
    return vy0 + uz * (vy1 - vy0)


# =============================================================================
# Python model of FBM functions (from T-DEMO-1.31, spec: FORMULAS.md VOL 10
# line 33, line 462)
#
# fBM: Value = Sum_i(Amplitude^i * Noise(Position * Frequency^i))
# Canonical defaults: lacunarity=2.0, gain=0.5, octaves=8
# =============================================================================

def _sum_amplitudes(octaves: int, gain: float) -> float:
    """Sum of geometric series: 1 + g + g^2 + ... + g^(octaves-1)."""
    if abs(gain - 1.0) < 1e-12:
        return float(octaves)
    return (1.0 - gain ** octaves) / (1.0 - gain)


def py_fbm_1d(p: float, octaves: int, lacunarity: float, gain: float) -> float:
    """FBM 1D: layered value noise."""
    value = 0.0
    amplitude = 1.0
    frequency = 1.0
    max_amplitude = 0.0
    for _ in range(octaves):
        value += amplitude * py_value_noise_1d(p * frequency)
        max_amplitude += amplitude
        frequency *= lacunarity
        amplitude *= gain
    if max_amplitude < 1e-8:
        return 0.0
    return value / max_amplitude


def py_fbm_2d(p, octaves: int, lacunarity: float, gain: float) -> float:
    """FBM 2D: layered value noise."""
    value = 0.0
    amplitude = 1.0
    frequency = 1.0
    max_amplitude = 0.0
    for _ in range(octaves):
        fx = p[0] * frequency
        fy = p[1] * frequency
        value += amplitude * py_value_noise_2d((fx, fy))
        max_amplitude += amplitude
        frequency *= lacunarity
        amplitude *= gain
    if max_amplitude < 1e-8:
        return 0.0
    return value / max_amplitude


def py_fbm_3d(p, octaves: int, lacunarity: float, gain: float) -> float:
    """FBM 3D: layered value noise."""
    value = 0.0
    amplitude = 1.0
    frequency = 1.0
    max_amplitude = 0.0
    for _ in range(octaves):
        fx = p[0] * frequency
        fy = p[1] * frequency
        fz = p[2] * frequency
        value += amplitude * py_value_noise_3d((fx, fy, fz))
        max_amplitude += amplitude
        frequency *= lacunarity
        amplitude *= gain
    if max_amplitude < 1e-8:
        return 0.0
    return value / max_amplitude


def py_fbm_perlin_3d(p, octaves: int, lacunarity: float, gain: float) -> float:
    """FBM 3D: layered Perlin noise (gradient-based)."""
    value = 0.0
    amplitude = 1.0
    frequency = 1.0
    max_amplitude = 0.0
    for _ in range(octaves):
        fx = p[0] * frequency
        fy = p[1] * frequency
        fz = p[2] * frequency
        value += amplitude * py_perlin_noise_3d((fx, fy, fz))
        max_amplitude += amplitude
        frequency *= lacunarity
        amplitude *= gain
    if max_amplitude < 1e-8:
        return 0.0
    return value / max_amplitude


# =============================================================================
# Python model of Domain Warp functions (T-DEMO-1.33)
#
# Spec formula (FORMULAS.md VOL 10 line 464):
#   Final_Noise = fBM(Position + vec2(fBM(Position), fBM(Position + 5.2)))
#
# The canonical implementation uses:
#   warp_vec = strength * fbm_warp(p + seed_offset)
#   warped_p = p + warp_vec
#   result = fbm_base(warped_p)
#
# where different seed offsets decorrelate the warp components:
#   2D: warp_y uses offset 100.0
#   3D: warp_y uses offset 100.0, warp_z uses offset 200.0
# =============================================================================

# Default parameters used throughout testing
DEFAULT_OCTAVES = 8
DEFAULT_LACUNARITY = 2.0
DEFAULT_GAIN = 0.5
DEFAULT_STRENGTH = 1.0

# Numerical tolerances
TOL_ABS = 1e-9
TOL_FINITE_CHECK = 1e-12


def domain_warp_2d(
    p,
    strength: float = DEFAULT_STRENGTH,
    warp_octaves: int = DEFAULT_OCTAVES,
    warp_lacunarity: float = DEFAULT_LACUNARITY,
    warp_gain: float = DEFAULT_GAIN,
    base_octaves: int = DEFAULT_OCTAVES,
    base_lacunarity: float = DEFAULT_LACUNARITY,
    base_gain: float = DEFAULT_GAIN,
) -> float:
    """Domain-warped FBM 2D (value noise base).

    Implements the spec formula:
      warp_x = fbm_2d(p, warp_params)
      warp_y = fbm_2d(p + (100, 100), warp_params)
      warped_p = p + strength * (warp_x, warp_y)
      result = fbm_2d(warped_p, base_params)

    The 100.0 offset decorrelates warp_x and warp_y so the warp field
    does not collapse to a 1D deformation along the diagonal.
    """
    warp_x = py_fbm_2d(p, warp_octaves, warp_lacunarity, warp_gain)
    warp_y = py_fbm_2d(
        (p[0] + 100.0, p[1] + 100.0),
        warp_octaves, warp_lacunarity, warp_gain,
    )

    warped_p = (p[0] + strength * warp_x, p[1] + strength * warp_y)

    return py_fbm_2d(warped_p, base_octaves, base_lacunarity, base_gain)


def domain_warp_3d(
    p,
    strength: float = DEFAULT_STRENGTH,
    warp_octaves: int = DEFAULT_OCTAVES,
    warp_lacunarity: float = DEFAULT_LACUNARITY,
    warp_gain: float = DEFAULT_GAIN,
    base_octaves: int = DEFAULT_OCTAVES,
    base_lacunarity: float = DEFAULT_LACUNARITY,
    base_gain: float = DEFAULT_GAIN,
) -> float:
    """Domain-warped FBM 3D (value noise base).

    Implements the spec formula:
      warp_x = fbm_3d(p, warp_params)
      warp_y = fbm_3d(p + (100,100,100), warp_params)
      warp_z = fbm_3d(p + (200,200,200), warp_params)
      warped_p = p + strength * (warp_x, warp_y, warp_z)
      result = fbm_3d(warped_p, base_params)

    Offsets at 100 and 200 decorrelate all three warp components.
    """
    warp_x = py_fbm_3d(p, warp_octaves, warp_lacunarity, warp_gain)
    warp_y = py_fbm_3d(
        (p[0] + 100.0, p[1] + 100.0, p[2] + 100.0),
        warp_octaves, warp_lacunarity, warp_gain,
    )
    warp_z = py_fbm_3d(
        (p[0] + 200.0, p[1] + 200.0, p[2] + 200.0),
        warp_octaves, warp_lacunarity, warp_gain,
    )

    warped_p = (
        p[0] + strength * warp_x,
        p[1] + strength * warp_y,
        p[2] + strength * warp_z,
    )

    return py_fbm_3d(warped_p, base_octaves, base_lacunarity, base_gain)


def domain_warp_perlin_3d(
    p,
    strength: float = DEFAULT_STRENGTH,
    warp_octaves: int = DEFAULT_OCTAVES,
    warp_lacunarity: float = DEFAULT_LACUNARITY,
    warp_gain: float = DEFAULT_GAIN,
    base_octaves: int = DEFAULT_OCTAVES,
    base_lacunarity: float = DEFAULT_LACUNARITY,
    base_gain: float = DEFAULT_GAIN,
) -> float:
    """Domain-warped Perlin FBM 3D (gradient-based).

    Same structure as domain_warp_3d but uses Perlin-based FBM for both
    the warp field and the base evaluation. Produces visually smoother
    warp fields with fewer grid artifacts.
    """
    warp_x = py_fbm_perlin_3d(p, warp_octaves, warp_lacunarity, warp_gain)
    warp_y = py_fbm_perlin_3d(
        (p[0] + 100.0, p[1] + 100.0, p[2] + 100.0),
        warp_octaves, warp_lacunarity, warp_gain,
    )
    warp_z = py_fbm_perlin_3d(
        (p[0] + 200.0, p[1] + 200.0, p[2] + 200.0),
        warp_octaves, warp_lacunarity, warp_gain,
    )

    warped_p = (
        p[0] + strength * warp_x,
        p[1] + strength * warp_y,
        p[2] + strength * warp_z,
    )

    return py_fbm_perlin_3d(warped_p, base_octaves, base_lacunarity, base_gain)


