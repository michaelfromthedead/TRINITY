# SECTION 14 -- Decorrelation of warp components
# =============================================================================


class TestDecorrelation:
    """Tests that warp components are decorrelated via seed offsets.

    The spec says different seed offsets decorrelate the warp components
    so the warp field does not collapse to a 1D deformation along the
    diagonal. Without decorrelation, warp_x and warp_y would be identical
    (same FBM evaluation at the same point), and the warp would always
    push along the (1, 1) diagonal.
    """

    def test_2d_warp_x_differs_from_warp_y(self):
        """warp_x and warp_y values should differ at typical points.

        Because warp_y is evaluated at p + (100, 100), it decorrelates
        from warp_x. The hash noise at offset position should produce
        different values.
        """
        differences = 0
        for ix in range(-5, 6):
            for iy in range(-5, 6):
                p = (ix * 0.3, iy * 0.3)
                wx = py_fbm_2d(p, 4, 2.0, 0.5)
                wy = py_fbm_2d(
                    (p[0] + 100.0, p[1] + 100.0), 4, 2.0, 0.5,
                )
                if abs(wx - wy) > TOL_ABS:
                    differences += 1
        assert differences > len(range(-5, 6)) * len(range(-5, 6)) * 0.5, (
            "warp_x and warp_y should differ at most points"
        )

    def test_3d_warp_components_differ(self):
        """All three 3D warp components should differ."""
        diffs_xy = 0
        diffs_xz = 0
        for ix in range(-3, 4):
            for iy in range(-3, 4):
                for iz in range(-3, 4):
                    p = (ix * 0.5, iy * 0.5, iz * 0.5)
                    wx = py_fbm_3d(p, 4, 2.0, 0.5)
                    wy = py_fbm_3d(
                        (p[0] + 100.0, p[1] + 100.0, p[2] + 100.0), 4, 2.0, 0.5,
                    )
                    wz = py_fbm_3d(
                        (p[0] + 200.0, p[1] + 200.0, p[2] + 200.0), 4, 2.0, 0.5,
                    )
                    if abs(wx - wy) > TOL_ABS:
                        diffs_xy += 1
                    if abs(wx - wz) > TOL_ABS:
                        diffs_xz += 1
        total = 7 * 7 * 7  # -3 to 3
        assert diffs_xy > total * 0.5, (
            "warp_x and warp_y should differ at most 3D points"
        )
        assert diffs_xz > total * 0.5, (
            "warp_x and warp_z should differ at most 3D points"
        )

    def test_decorrelation_prevents_diagonal_collapse(self):
        """Decorrelation prevents the warp from collapsing to 1D.

        Without decorrelation, all warp components would be equal and
        the warp would push along (1,1,1) diagonal. With decorrelation,
        the warp displacement direction varies across the domain.
        """
        directions = set()
        for ix in range(-3, 4):
            for iy in range(-3, 4):
                p = (ix * 0.5, iy * 0.5)
                wx = py_fbm_2d(p, 4, 2.0, 0.5)
                wy = py_fbm_2d(
                    (p[0] + 100.0, p[1] + 100.0), 4, 2.0, 0.5,
                )
                # Round to 2 decimal places to bin directions
                ratio = (wx / wy) if abs(wy) > 0.01 else 0.0
                directions.add(round(ratio, 2))
        # Should have multiple distinct displacement directions
        assert len(directions) > 3, (
            f"Expected multiple warp directions, got {len(directions)}"
        )


