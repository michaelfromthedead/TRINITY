class TestDomainWarp2DComplexity:
    """Tests that domain warping increases visual complexity.

    The spec acceptance criterion is "increased visual complexity through
    non-linear coordinate deformation." This means the warped output
    should be more complex (richer detail) than plain FBM.
    """

    def test_warped_more_complex_than_plain(self):
        """Warped FBM should have higher adjacent variation than plain FBM.

        Adjacent variation measures how much the signal changes between
        nearby samples. Higher variation = more fine detail = higher
        visual complexity.
        """
        # Plain FBM strip
        plain_strip = [
            py_fbm_2d((i * 0.01, 0.5), DEFAULT_OCTAVES, DEFAULT_LACUNARITY, DEFAULT_GAIN)
            for i in range(300)
        ]
        # Warped FBM strip (same FBM params for both warp and base)
        warped_strip = _sample_2d_strip(
            domain_warp_2d, n=300, x_step=0.01,
            strength=2.0,
        )

        plain_var = _adjacent_variation(plain_strip)
        warped_var = _adjacent_variation(warped_strip)

        # Warped should have higher adjacent variation (more fine detail)
        assert warped_var > plain_var, (
            f"Warped FBM adjacent variation ({warped_var:.6f}) should be "
            f"> plain FBM ({plain_var:.6f})"
        )

    def test_warped_more_zero_crossings(self):
        """Warped FBM should have more zero crossings (higher frequency)."""
        plain_strip = [
            py_fbm_2d((i * 0.02, 0.5), DEFAULT_OCTAVES, DEFAULT_LACUNARITY, DEFAULT_GAIN)
            for i in range(200)
        ]
        warped_strip = _sample_2d_strip(
            domain_warp_2d, n=200, x_step=0.02,
            strength=2.0,
        )

        zc_plain = _zero_crossings(plain_strip)
        zc_warped = _zero_crossings(warped_strip)

        # Warped should have more zero crossings (richer frequency content)
        assert zc_warped >= zc_plain, (
            f"Warped FBM zero crossings ({zc_warped}) should be >= "
            f"plain FBM ({zc_plain})"
        )

    def test_warped_not_just_scaled_plain(self):
        """Warped FBM should not be a simple scaling of plain FBM.

        Domain warping is a non-linear deformation, so the warped output
        should not be linearly correlated with plain FBM.
        """
        samples_plain = [
            py_fbm_2d((i * 0.05, 0.5), DEFAULT_OCTAVES, DEFAULT_LACUNARITY, DEFAULT_GAIN)
            for i in range(100)
        ]
        samples_warped = [
            domain_warp_2d((i * 0.05, 0.5), strength=2.0)
            for i in range(100)
        ]

        # Compute correlation coefficient
        mean_p = _mean(samples_plain)
        mean_w = _mean(samples_warped)
        num = sum((a - mean_p) * (b - mean_w) for a, b in zip(samples_plain, samples_warped))
        den = math.sqrt(
            sum((a - mean_p) ** 2 for a in samples_plain) *
            sum((b - mean_w) ** 2 for b in samples_warped)
        )
        corr = num / den if den > 0 else 0.0

        # Correlation should not be near 1.0 (warping is non-linear)
        assert corr < 0.95, (
            f"Warped and plain FBM too highly correlated: {corr:.4f}"
        )

    def test_warped_variance_differs(self):
        """Warped FBM should have different variance from plain FBM.

        The non-linear deformation should alter the statistical distribution
        of the output values.
        """
        plain_vals = [
            py_fbm_2d((i * 0.05, j * 0.07), DEFAULT_OCTAVES, DEFAULT_LACUNARITY, DEFAULT_GAIN)
            for i in range(-10, 11) for j in range(-10, 11)
        ]
        warped_vals = [
            domain_warp_2d((i * 0.05, j * 0.07), strength=2.0)
            for i in range(-10, 11) for j in range(-10, 11)
        ]

        var_plain = _variance(plain_vals)
        var_warped = _variance(warped_vals)

        # The variance may go up or down depending on strength,
        # but it should not be identical (domain warping scrambles
        # the spatial distribution, altering variance)
        assert abs(var_warped - var_plain) > 1e-6, (
            f"Warped variance ({var_warped:.4f}) should differ from "
            f"plain variance ({var_plain:.4f})"
        )


