class TestDomainWarp2DRange:
    """Tests that domain_warp_2d produces bounded, finite output."""

    def test_output_finite_default_params(self):
        """Default parameter output must be finite."""
        for ix in range(-10, 11):
            for iy in range(-10, 11):
                p = (ix * 0.3, iy * 0.3)
                v = domain_warp_2d(p)
                assert math.isfinite(v), f"domain_warp_2d({p}) not finite: {v}"

    def test_output_bounded(self):
        """Output should be approximately in [-1, 1], inherited from FBM base."""
        values = []
        for ix in range(-10, 11):
            for iy in range(-10, 11):
                p = (ix * 0.3, iy * 0.3)
                v = domain_warp_2d(p)
                values.append(v)
                assert -2.0 <= v <= 2.0, (
                    f"domain_warp_2d({p}) = {v} outside expected range"
                )

    def test_output_bounded_various_strengths(self):
        """Output should remain bounded for various warp strengths."""
