def generate_scene_wgsl(
    scene: FullSceneNode,
    *,
    include_uniforms: bool = True,
    include_bindings: bool = True,
) -> str:
    """Generate complete WGSL compute shader for a scene.

    Args:
        scene: FullSceneNode containing all scene components.
        include_uniforms: Whether to include uniform buffer definitions.
        include_bindings: Whether to include bind group definitions.

    Returns:
        Complete WGSL compute shader as a string.

    Example:
        >>> from engine.rendering.demoscene.ast_nodes import (
        ...     SceneGraph, SphereNode, PositionNode, FloatNode, FullSceneNode
        ... )
        >>> graph = SceneGraph(primitives=(SphereNode(PositionNode(), FloatNode(1.0)),))
        >>> scene = FullSceneNode(scene_graph=graph, name="demo")
        >>> wgsl = generate_scene_wgsl(scene)
        >>> "@compute" in wgsl
        True
    """
    gen = SceneCodegen()
    return gen.generate(
        scene,
        include_uniforms=include_uniforms,
        include_bindings=include_bindings,
    )


def generate_compute_shader(
    scene: FullSceneNode,
    *,
    include_uniforms: bool = True,
    include_bindings: bool = True,
) -> str:
    """Alias for generate_scene_wgsl for clarity.

    Args:
        scene: FullSceneNode containing all scene components.
        include_uniforms: Whether to include uniform buffer definitions.
        include_bindings: Whether to include bind group definitions.

    Returns:
        Complete WGSL compute shader as a string.
    """
    return generate_scene_wgsl(
        scene,
        include_uniforms=include_uniforms,
        include_bindings=include_bindings,
    )
