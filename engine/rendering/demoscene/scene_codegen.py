PBR_LIGHTING = """\
/// Fresnel-Schlick approximation.
fn fresnel_schlick(cos_theta: f32, f0: vec3<f32>) -> vec3<f32> {
    return f0 + (1.0 - f0) * pow(clamp(1.0 - cos_theta, 0.0, 1.0), 5.0);
}

/// GGX/Trowbridge-Reitz normal distribution.
fn distribution_ggx(n: vec3<f32>, h: vec3<f32>, roughness: f32) -> f32 {
    let a = roughness * roughness;
    let a2 = a * a;
    let n_dot_h = max(dot(n, h), 0.0);
    let n_dot_h2 = n_dot_h * n_dot_h;

    let num = a2;
    let denom = n_dot_h2 * (a2 - 1.0) + 1.0;
    let denom2 = 3.14159265359 * denom * denom;

    return num / max(denom2, 0.0001);
}

/// Schlick-GGX geometry function.
fn geometry_schlick_ggx(n_dot_v: f32, roughness: f32) -> f32 {
    let r = roughness + 1.0;
    let k = (r * r) / 8.0;
    return n_dot_v / (n_dot_v * (1.0 - k) + k);
}

/// Smith's geometry function.
fn geometry_smith(n: vec3<f32>, v: vec3<f32>, l: vec3<f32>, roughness: f32) -> f32 {
    let n_dot_v = max(dot(n, v), 0.0);
    let n_dot_l = max(dot(n, l), 0.0);
    let ggx1 = geometry_schlick_ggx(n_dot_v, roughness);
    let ggx2 = geometry_schlick_ggx(n_dot_l, roughness);
    return ggx1 * ggx2;
}

/// Calculates PBR lighting for a surface point.
///   p          -- surface position
///   n          -- surface normal
///   v          -- view direction (towards camera)
///   mat        -- material properties
///   returns    -- final color
fn calculate_lighting(
    p: vec3<f32>,
    n: vec3<f32>,
    v: vec3<f32>,
    mat: Material,
) -> vec3<f32> {
    // Dielectric F0 = 0.04, metallic uses albedo
    let f0 = mix(vec3<f32>(0.04), mat.albedo, mat.metallic);

    var lo = vec3<f32>(0.0);

    // Process each light (inline for now, generated per-scene)
    lo = lo + calculate_light_contribution(p, n, v, mat, f0);

    // Ambient term
    let ambient = vec3<f32>(0.03) * mat.albedo * mat.ambient_occlusion;

    // Emission
    let emission = mat.emission;

    return ambient + lo + emission;
}
"""


