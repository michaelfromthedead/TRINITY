RAY_GENERATION = """\
/// Generates a ray direction from screen coordinates.
///   uv         -- normalized screen coordinates [-1, 1]
///   origin     -- camera position
///   target     -- look-at point
///   up         -- up vector
///   fov        -- field of view in degrees
///   aspect     -- aspect ratio (width / height)
///   returns    -- normalized ray direction
fn generate_ray(
    uv: vec2<f32>,
    origin: vec3<f32>,
    target: vec3<f32>,
    up: vec3<f32>,
    fov: f32,
    aspect: f32,
) -> vec3<f32> {
    let forward = normalize(target - origin);
    let right = normalize(cross(forward, up));
    let up_corrected = cross(right, forward);

    let fov_rad = fov * 3.14159265359 / 180.0;
    let half_height = tan(fov_rad * 0.5);
    let half_width = half_height * aspect;

    let rd = normalize(
        forward +
        right * uv.x * half_width +
        up_corrected * uv.y * half_height
    );

    return rd;
}
"""


# =============================================================================
# RAY MARCHING
# =============================================================================

RAY_MARCHING = """\
/// Result of ray marching.
struct RayHit {
    hit: bool,
    position: vec3<f32>,
    distance: f32,
    material_id: u32,
    steps: u32,
}

/// Marches a ray through the scene using sphere tracing.
///   ro         -- ray origin
///   rd         -- ray direction (normalized)
///   max_steps  -- maximum number of marching steps
///   max_dist   -- maximum ray distance
///   epsilon    -- surface hit threshold
///   returns    -- RayHit with hit info
fn ray_march(
    ro: vec3<f32>,
    rd: vec3<f32>,
    max_steps: u32,
    max_dist: f32,
    epsilon: f32,
) -> RayHit {
    var t: f32 = 0.0;
    var hit = RayHit(false, vec3<f32>(0.0), 0.0, 0u, 0u);

    for (var i = 0u; i < max_steps; i = i + 1u) {
        let p = ro + rd * t;
        let result = scene_sdf(p);
        let d = result.x;
        let mat_id = u32(result.y);

        if (d < epsilon) {
            hit.hit = true;
            hit.position = p;
            hit.distance = t;
            hit.material_id = mat_id;
            hit.steps = i;
            return hit;
        }

        t = t + d;

        if (t > max_dist) {
            hit.steps = i;
            return hit;
        }
    }

    hit.steps = max_steps;
    return hit;
}
"""


