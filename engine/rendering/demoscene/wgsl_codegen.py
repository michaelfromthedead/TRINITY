_DOMAIN_OP_CALLS = {
    RepeatNode: "domain_repeat({p}, {cell_size})",
    CellIdNode: "domain_cell_id({p}, {cell_size})",
    MirrorNode: "domain_mirror_{axis}({p})",
    KifsNode: "domain_kifs({p}, {folds})",
    TwistNode: "domain_twist({p}, {rate})",
    BendNode: "domain_bend({p}, {radius})",
    StretchNode: "domain_stretch_{axis}({p}, {stretch})",
}

_KIFS_COMP = "domain_kifs_compensation({folds})"
_STRETCH_COMP = "domain_stretch_compensation({stretch})"


