def _fmt_float(val: float) -> str:
    if val == int(val) and not (val == 0.0 and str(val).startswith("-")):
        return f"{int(val)}.0"
    return f"{val}"


def _fmt_vec3(v: Vec3Node) -> str:
    return (
        f"vec3<f32>("
        f"{_fmt_float(v.x)}, {_fmt_float(v.y)}, {_fmt_float(v.z)}"
        f")"
    )


